    function previewFile(input) {
      const file = input.files[0];
      if (file) {
        const reader = new FileReader();
        reader.onload = e => {
          $('#previewImg').attr('src', e.target.result);
          $('#studentPicture').val(e.target.result);
        };
        reader.readAsDataURL(file);
      }
